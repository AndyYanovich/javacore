package com.kitcenter.app.homework.lesson4;

public class PrimitiveConvertor {

    public char floatToChar(float value) {
        char charVar = (char) value;
        return charVar;
    }

    public char intToChar(int value) {
        char charVar = (char) value;
        return charVar;
    }

    public int charToInt(char value) {
        int intVar = (int) value;
        return intVar;
    }
}